const { filter, getAll, getAllByStatusActive } = require("../Services/MobileBrand.Service");
const { sendError, sendSuccess } = require("./Controller");

const handleGetAll = async (req, res) => {
  const result = await getAll();
  if (result.success)
    return sendSuccess(res, result.data, result.message, result.status);
  return sendError(res, result.message, result.status);
};

const handleGetAllActive = async (req, res) => {
  const result = await getAllByStatusActive();
  if (result.success)
    return sendSuccess(res, result.data, result.message, result.status);
  return sendError(res, result.message, result.status);
};

const handleFilter = async (req, res) => {
  const result = await filter(req.query);
  if (result.success)
    return sendSuccess(res, result.data, result.message, result.status);
  return sendError(res, result.message, result.status);
};

module.exports = {
  handleFilter,
  handleGetAll,
  handleGetAllActive
};
